DROP TABLE SUPPORT;
DROP TABLE SPONSORS;
DROP TABLE LEAGUES;
DROP TABLE TEAMS;
DROP TABLE REGIONS;

CREATE TABLE SPONSORS (
  SID          INTEGER,
  SPONSOR_NAME VARCHAR2(30),
  MARKET_VALUE FLOAT,
  PRIMARY KEY (SID)
);

CREATE TABLE TEAMS (
  TID         INTEGER,
  TEAM_NAME   VARCHAR2(30),
  AVERAGE_AGE FLOAT,
  PRIMARY KEY (TID)
);

CREATE TABLE REGIONS (
  RID              INTEGER,
  REGION_NAME      VARCHAR2(30),
  FOOTBALL_RANKING INTEGER,
  PRIMARY KEY (RID)
);

CREATE TABLE LEAGUES (
  LID          INTEGER,
  LEAGUE_NAME  VARCHAR2(30),
  CHAMPION_TID INTEGER,
  YEAR         INTEGER,
  SEASON       VARCHAR2(10),
  RID          INTEGER,
  PRIMARY KEY (LID),
  FOREIGN KEY (CHAMPION_TID) REFERENCES TEAMS (TID),
  FOREIGN KEY (RID) REFERENCES REGIONS (RID)
);

CREATE TABLE SUPPORT (
  LID         INTEGER,
  SID         INTEGER,
  SPONSORSHIP FLOAT,
  PRIMARY KEY (LID, SID),
  FOREIGN KEY (LID) REFERENCES LEAGUES (LID),
  FOREIGN KEY (SID) REFERENCES SPONSORS (SID)
);

